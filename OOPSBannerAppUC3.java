/**
 * OOPSBannerApp UC3 - OOPS Banner Application
 * Using String.join() for memory efficiency.
 */
public class OOPSBannerAppUC3 {

    public static void main(String[] args) {
        System.out.println(String.join("  ", "  *** ", "  ***  ", "***** ", " ****"));
        System.out.println(String.join("  ", "**   **", "**  **", "**  **", "**   "));
        System.out.println(String.join("  ", "**   **", "**  **", "**  **", "**   "));
        System.out.println(String.join("  ", "**   **", "**  **", "******", " *** "));
        System.out.println(String.join("  ", "**   **", "**  **", "** ", "      **"));
        System.out.println(String.join("  ", "**   **", "**  **", "** ", "      **"));
        System.out.println(String.join("  ", "  *** ", "  *** ", " ** ", "  **** "));
    }
}